    private void BTCambiarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTCambiarProductoActionPerformed
        // TODO add your handling code here:
        String aux=(String) CBProductos.getSelectedItem();
        String id = aux.trim().split("\\s+")[0]; //va obtener la primera palabra del combo box osea el ID
        Producto nuevo=Gestor.productos.findProducto(id); //Busca en la base de datos
        nuevo.setDescripcion(TFDescrip.getText());
        nuevo.setMarca(TFMarca.getText());
        nuevo.setNombre(TFNombre.getText());
        String precioaux=TFPrecio.getText();
        Double precio=Double.parseDouble(precioaux);
        nuevo.setPrecio(precio);
        String stockaux=TFStock.getText();
        int stock=Integer.parseInt(stockaux);
        nuevo.setStock(stock);
        Gestor.productos.edit(nuevo); //hace el cambio
        TFDescrip.setText("");
        TFMarca.setText("");
        TFNombre.setText("");
        TFPrecio.setText("");
        TFStock.setText("");
        LlEstado.setText("Estado: Producto Actualizado");
        LlEstado.setText("Estado: Producto Actualizado");
        
    }//GEN-LAST:event_BTCambiarProductoActionPerformed


