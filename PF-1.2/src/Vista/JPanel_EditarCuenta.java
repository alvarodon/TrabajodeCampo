    private void BTModificarCuentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTModificarCuentaActionPerformed
        // TODO add your handling code here:
        String id=LlID.getText();
        String contra=TFContra.getText();
        String nombre=TFNombre.getText();
        String apellido1=TFApellido1.getText();
        String apellido2=TFApellido2.getText();
         if (usuario instanceof Vendedor){
             Vendedor nuevo=Gestor.vendedores.findVendedor(id);
             nuevo.setContrania(contra);
             nuevo.setNombre(nombre);
             nuevo.setApellido1(apellido1);
             nuevo.setApellido2(apellido2);
             Gestor.vendedores.edit(nuevo);
         }
         if (usuario instanceof Comprador){
             String aux=TFMonto.getText();
             Double monto=Double.parseDouble(aux);
             Comprador nuevo=Gestor.compradores.findComprador(id);
             nuevo.setContrania(contra);
             nuevo.setNombre(nombre);
             nuevo.setApellido1(apellido1);
             nuevo.setApellido2(apellido2);
             nuevo.setDinero(monto);
             Gestor.compradores.edit(nuevo);
             TFMonto.setText("");
         }
         TFContra.setText("");
         TFContra.setText("");
         TFNombre.setText("");
         TFApellido1.setText("");
         TFApellido2.setText("");
         LlEstado.setText("Estado: Cuenta Actualizada");
         
    }//GEN-LAST:event_BTModificarCuentaActionPerformed


