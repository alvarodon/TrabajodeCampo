    public JFrame_Comprador(String id) {
        this.id=id;
        this.comprador=Gestor.compradores.findComprador(id);
        initComponents();
        setResizable(false);
        setLocationRelativeTo(null);
        JLabelUsuario.setText("Bienvenido: "+comprador.getNombre()+" "+comprador.getApellido1()+" "+comprador.getApellido2());
        JLabelID.setText("ID: "+comprador.getId());
        List<Producto>produc=Gestor.productos.findProductoEntities();
        int[][] coordenadas = {
            {0, 0},      // Producto 1
            {185, 0},    // Producto 2
            {370, 0},    // Producto 3
            {0, 195},    // Producto 4
            {185, 195},  // Producto 5
            {370, 195}   // Producto 6
         };
        jPanel1.setLayout(new BoxLayout(jPanel1, BoxLayout.Y_AXIS));
        for (int i = 0; i < produc.size(); i++) {
              int x = coordenadas[i][0];
              int y = coordenadas[i][1];
                Producto nuevo = produc.get(i);
                JPanel_Producto nuevopanel = new JPanel_Producto(nuevo);
                nuevopanel.setBounds(x, y, 175, 185);
            
                // AÃ±adir al JPanel1, no al JFrame
                     jPanel1.add(nuevopanel);
        
    }
    jPanel1.revalidate();
    jPanel1.repaint();

    }

