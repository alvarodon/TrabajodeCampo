    private void BTCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTCrearActionPerformed
        // TODO add your handling code here:
        String tipo=(String) CBTipodeCuenta.getSelectedItem();
        String id=TFID.getText();
        String contra=TFContra.getText();
        String nombre=TFNombre.getText();
        String apellido1=TFApellido1.getText();
        String apellido2=TFApellido2.getText();

        if(tipo.contains("Comprador")){
            String auxmontoinicial=TFMontoInicial.getText();
            double montoinicial=Double.parseDouble(auxmontoinicial);
            Comprador nuevo=new Comprador(montoinicial,id,contra,nombre,apellido1,apellido2);
            Gestor.compradores.create(nuevo);
        }else if(tipo.contains("Vendedor")){
            Vendedor nuevo=new Vendedor(id,contra,nombre,apellido1,apellido2);
            Gestor.vendedores.create(nuevo);
        }
        TFID.setText("");
        TFContra.setText("");
        TFNombre.setText("");
        TFApellido1.setText("");
        TFApellido2.setText("");
        LabelEstado.setText("Estado: Cuenta Creada del tipo "+tipo);

    }//GEN-LAST:event_BTCrearActionPerformed

